#include <stdio.h>

void vertices(float Bx, float By, float a, float altitude, float* Ax, float* Ay) {
    float Cx = a, Cy = 0;  // Coordinates of point C

    // Calculate midpoint L of BC
    float Lx = (Bx + Cx) / 2;
    float Ly = 0;  // Since it's on the x-axis, Ly = 0

    // Coordinates of A (Ax, Ay)
    &Ax = Lx;
    &Ay = altitude;
}


